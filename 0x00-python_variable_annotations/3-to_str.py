#!/usr/bin/env python3
"""Module for converting a float to its string representation.

Module provides a function that converts a float to its string representation.
"""


def to_str(n: float) -> str:
    """Returns the string representation of the float.

    Args:
        n (float): The float number to convert to a string.

    Returns:
        str: The string representation of `n`.
    """
    return str(n)
